package com.cognitran.classes.topic1;

public class MathExample {

    public static void main(String[] args) {
        int a = 2;
        int b = 3;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        int c = a + b;

        System.out.println("a + b = " + c);

        c = a * b;
        System.out.println("a * b = " + c);

    }
}
